   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Leaf senescence has been recognized as the last phase of plant development, a highly ordered process regulated by genes known as senescence associated genes (SAGs). Leaf senescence is induced as part of plant development but can also be prematurely induced as a result of environmental changes or harvesting. Premature senescence leads to reduced yield and quality of crops and this is likely to be of increasing concern in times of climate change and parallel population growth. Therefore, it might be expected that more research should be focused on this important topic in the future as increased understanding of the senescence traits will lead to the development of crops with improved yield, stress tolerance and shelf life. At present, great progresses have been achieved in leaf senescence research using Arabidopsis thaliana. However, how the leaf senescence process is initiated and  regulated remains largely unanswered. Luckily, with the development of genome research, more and more plant genome had been sequenced and analyzed, which offer us a good opportunity to analyze homologous gene of SAGs found in Arabidopsis thaliana. <br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;By integrating the data from mutant studies, transgenic analysis and gene ontology annotation, we identify genes related to regulation of the leaf senescence in various species, including Arabidopsis thaliana, Oryza sativa, Pisum sativum, Solanum lycopersicum cv. MicroTom, Brassica oleracea L. var. Italica, Medicago sativa, Solanum tuberosum L. cv. D, Glycine max, Hordeum vulgare, Zea mays, Rosa hybrid, etc. <br/>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Another pronounced characteristic of this database is that we have categorized these genes according to their functions in regulation leaf senescence and developed ontology to precisely describe senescence-associated phenotypes with standardized vocabularies. </p>
   
