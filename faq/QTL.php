<p style="margin-left:20px"><font  size="2"> 

Many researchers have demonstrated that there is a positive correlation between the crop yield and the duration of green leaf, so the stay-green traits strongly attract crop breeders. Functional stay-green genotypes can increase photosynthetic capacity of plants in the final growth stage, resulting in higher yield, increased nutritional quality of stems or leaves for herbivorous animals and strengthened tolerance to biotic or abiotic stresses.<br/><br/>
Currently, many stay-green Quantitative Trait Loci (QTLs) especially in rice, maize, barley, and sorghum are found and deposited into <a href="http://www.gramene.org/qtl/" target="_blank"><b>Gramene QTL Database</b></a>. In order to help researchers in the field of leaf senescence get these resources easily, we collected stay-green QTLs in LSD and provided their links to Gramene QTL Database.<br/>
